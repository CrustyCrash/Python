# 1 Accept two numbers from the user and print 
# a) addition 
# b) first number squared 2 
# c) first number raised to second number

print("Enter first num :")
first_var = int(input())
print("Enter second num :")
sec_var = int(input())

#Addition

print(first_var, " + ", sec_var, " = ", first_var + sec_var)

# first number squared

print("Square of ", first_var, "is:", first_var * first_var)

print(first_var, "^", sec_var, " = ", (first_var ** sec_var))
